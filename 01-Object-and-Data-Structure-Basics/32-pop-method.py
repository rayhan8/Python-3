# pop()

# remove item at the end of the list
my_list = ['one', 'two', 'three', 'four', 'five']
my_list.pop()
print(my_list)

# remove item in selected index position
list_1 = ['dollar', 'rayhan', 'rafin', 'tahjid']
list_1.pop(1)
print(list_1)

# remove item in negative index position
list_2 = ['one', 'two', 'three', 'four']
list_2.pop(-1)
print(list_2)
